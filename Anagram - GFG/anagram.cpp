class Solution
{
    public:
    //Function is to check whether two strings are anagram of each other or not.
    bool isAnagram(string a, string b){
        
        // Your code here
        map<char,int>mp1;
        map<char,int>mp2;
        for(auto i: a)
        {
            mp1[i]++;
        }
        for(auto i:b)
        {
            mp2[i]++;
        }
        return mp1==mp2;
    }

};

